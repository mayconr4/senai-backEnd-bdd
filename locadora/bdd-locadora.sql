 USE locadora;
 
 CREATE TABLE cliente(
 id INT AUTO_INCREMENT PRIMARY KEY,
 nome VARCHAR(150) NOT NULL,
 cpf VARCHAR(20) NOT NULL,
 rg VARCHAR(20) NOT NULL,
 rne VARCHAR(20) NULL  
 );     
 ALTER TABLE cliente ADD cnh VARCHAR(20) NOT NULL;
