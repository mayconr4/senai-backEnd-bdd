 CREATE TABLE veiculo(
 id INT AUTO_INCREMENT PRIMARY KEY,
 marca VARCHAR(90) NOT NULL,
 placa VARCHAR(9) NOT NULL,      
 modelo VARCHAR(90) NOT NULL  ,
 ano INT NOT NULL  
 
 );   
 ALTER TABLE veiculo ADD combustivel VARCHAR(10) NOT NULL;
